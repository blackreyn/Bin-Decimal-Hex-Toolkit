# Conversión de Decimal a Hexadecimal
def decimal_a_hexadecimal(decimal):
    # Convertir de decimal a hexadecimal
    return hex(decimal)

# Definir el número decimal directamente en el código
numero_decimal = 10  # Cambia este valor según sea necesario

# Conversión de decimal a hexadecimal
resultado = decimal_a_hexadecimal(numero_decimal)
print(f"El número decimal {numero_decimal} en hexadecimal es: {resultado}")
